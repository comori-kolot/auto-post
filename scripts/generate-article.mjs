import OpenAI from "openai";
import { SITE_CONTEXT, ARTICLE_MODEL } from "./config.mjs";

const ARTICLE_SCHEMA = {
  name: "seo_article",
  strict: true,
  schema: {
    type: "object",
    properties: {
      title: {
        type: "string",
        description:
          "記事タイトル。32文字前後。助詞を省略せず自然な日本語にする（体言止めの単語を無理に並べない）。「とは」「方法」「ポイント」「注意点」などの共起語を自然に使う。比喩や煽り文句は使わない。",
      },
      metaDescription: { type: "string", description: "meta description（120文字以内）" },
      slug: { type: "string", description: "英数字とハイフンのみのURLスラッグ" },
      contentHtml: {
        type: "string",
        description:
          "WordPress投稿本文のHTML。3000文字以上。見出しは3〜5個程度に絞り込み、目次を作るための細切れ見出しは追加しない。各見出し配下は400字以上の読み応えのある文章にする。箇条書き<ul><li>は本当に必要な場面（チェックリスト等）のみ最小限にとどめ、基本は<p>段落の地の文で説明する。費用・料金に関わる話題の場合は具体的な金額目安を<table>で必ず示す。比較や選択肢の整理には<table>を積極的に使う。強調したい語句は<strong>、特に注目してほしい一文は<mark>で軽くハイライトする（使いすぎない）。1つの<p>は最大3文までとし、4文目からは新しい<p>に分ける（文字の塊が大きくなりすぎないようにする）。装飾的な前置きや結びの挨拶文は書かない。関連する行政・自治体・業界団体の公式サイト（林野庁、中小企業庁、全国木材組合連合会など実在が確実なトップレベルの公式ドメインのみ）へ<a href target=\"_blank\" rel=\"noopener\">の外部リンクを最低1つ含める。存在が不確かな深い階層のURLは絶対に作らない。加えて、最初のH2セクションの末尾と、最後のH2セクションの末尾の2箇所に、それぞれ次の形式で問い合わせ導線を必ず入れる： <p><strong><a href=\"https://greeen-biz.com/contact/\">（記事の対策テーマ）に関することなら、どんなことでも無料でご相談いただけます。お気軽にお問い合わせください。</a></strong></p> （（記事の対策テーマ）の部分はキーワード・記事内容に合わせて自然な言い回しに置き換える。2箇所とも同じ文言で構わない）",
      },
    },
    required: ["title", "metaDescription", "slug", "contentHtml"],
    additionalProperties: false,
  },
};

export async function generateArticle({ keyword, apiKey }) {
  const client = new OpenAI({ apiKey });

  const completion = await client.chat.completions.create({
    model: ARTICLE_MODEL,
    messages: [
      {
        role: "system",
        content: `あなたは${SITE_CONTEXT.siteName}のSEOコンテンツライターです。${SITE_CONTEXT.business} 文体: ${SITE_CONTEXT.tone}

禁止事項:
- AIが多用しがちな凝った比喩表現（「〜は羅針盤のような」「〜という航海」等）は一切使わない
- くどい言い回し・冗長な前置き・同じ内容の言い換え繰り返しをしない
- 誇張表現や煽り文句を使わない
- タイトルで助詞（「の」「と」「が」等）を省略して名詞を無理やり連結しない。声に出して自然な日本語にする
- 箇条書きを多用して内容を薄める「AIっぽい」構成にしない。1段落にきちんと内容を詰め込む
- 目次を意識した細かい見出し分割をしない。ページ構成の説明のような形式的な見出しは作らない

文章の書き方:
- 一文を短く、結論を先に、簡潔に
- 見出しは3〜5個に絞り、各見出し配下は具体例・数字・実務手順を含む読み応えのある文章にする
- タイトル・見出しは「とは」「方法」「ポイント」「注意点」「効果」「メリット」「デメリット」といった検索でよく使われる共起語を自然に使う
- 費用・料金に関する情報は必ず具体的な金額目安を示し、可能な限り<table>で整理する
- 比較・選択肢の整理には<table>、強調には<strong>、重要な一文には<mark>を使い、視覚的なメリハリをつける（<mark>は使いすぎない）
- 1つの<p>段落は最大3文まで。4文目からは必ず新しい<p>に分ける。文字の塊を大きくしすぎない
- 読者（製材工場・木材関連事業の経営者）目線で「本当に気になること」を優先する。一般論の各論を網羅的に並べるのではなく、Web集客・サイト制作の専門家だからこそ言える具体的で新しい視点・注意点を中心に書く
- 木材・林業業界は行政・自治体の補助金や政策動向の影響が大きいので、関連する公的機関・業界団体への外部リンクを最低1つ入れ、政策動向にどう向き合うべきかにも触れる
- 「独自性のある視点」とは平易な言葉で語られる具体的な情報のことであり、難解な言い回しや凝った比喩ではない。「方法」「注意点」「料金」「ポイント」のようなシンプルで分かりやすい言葉を積極的に使い続け、わかりやすさを絶対に犠牲にしない
- 記事は必ず「木材業界の集客」というテーマに沿った内容にする。木材・林業に直接関係しないキーワードで書く場合も、必ず集客・Web活用・経営という切り口に着地させる
- 最初のH2セクションの末尾と、最後のH2セクションの末尾の2箇所に、指定形式の問い合わせ導線（https://greeen-biz.com/contact/ へのリンク）を必ず入れる。これは省略不可の必須要素`,
      },
      {
        role: "user",
        content: `キーワード「${keyword}」で検索意図を満たすSEO記事を作成してください。読者（製材工場・木材関連事業の経営者層）が実務で使える具体的な費用感・判断基準・手順を、厚みのある文章で書いてください。ありきたりな一般論ではなく、Web業界の専門家だからこそ提供できる独自性のある実践的な情報を中心にしてください。`,
      },
    ],
    response_format: { type: "json_schema", json_schema: ARTICLE_SCHEMA },
  });

  const parsed = JSON.parse(completion.choices[0].message.content);
  return parsed;
}
